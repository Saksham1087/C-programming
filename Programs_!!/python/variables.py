#variables are basic data types in python
# python is dynamically typed , no explicit type declaration needed

x =10  # Integer
y = 28.5  # Float
name = "Saksham"  # String
is_present = True  # Boolean
items = [1,"three",6.9]  # List

print("Variable x:", x , "Type:",type(x))
print("Variable y:",y,"Type:", type(y))
print("Variable name:",name,"Type:", type(name))
print("Variable is_present:",is_present,"Type:",type(is_present))
print("Variable items:",items,"Type:",type(items))