// Variables and basic Data types in TypeScript
var x = 10; // Number type
var y = 20.5; // Number type
var name = "Saksham"; // String type
var isPresent = true; // Boolean type
var items = [1, 2, 3]; // Array type

console.log(`Variable x: ${x}, Type: ${typeof x}`);
console.log(`Variable y: ${y}, Type: ${typeof y}`);
console.log(`Variable name: ${name}, Type: ${typeof name}`);
console.log(`Variable isPresent: ${isPresent}, Type: ${typeof isPresent}`);
console.log(`Variable items: ${items}, Type: ${typeof items}`);
